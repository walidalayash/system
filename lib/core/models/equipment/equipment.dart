class Equipment {
  final String idUser;
  final String idEquipment;
  final String department;
  final String timeRequest;
  final String userName;
  final String typeEquipment;
  final String typeRequest;
  final String state;
  final String photo;

  Equipment({
    required this.idUser,
    required this.idEquipment,
    required this.department,
    required this.timeRequest,
    required this.userName,
    required this.typeEquipment,
    required this.typeRequest,
    required this.state,
    required this.photo,
  });
}
